////////////////////////////////////////////////////////////////////////////////
#ifndef GPIO_TYPE_ID_HPP
#define GPIO_TYPE_ID_HPP

////////////////////////////////////////////////////////////////////////////////
#include <gpio++/chip.hpp>
#include <gpio++/pin.hpp>

////////////////////////////////////////////////////////////////////////////////
namespace gpio
{

////////////////////////////////////////////////////////////////////////////////
// chip id
inline std::string type_id(const gpio::chip* chip)
{
    return chip->id().size()
        ? chip->type() + ':' + chip->id()
        : chip->type();
}

////////////////////////////////////////////////////////////////////////////////
// pin id
inline std::string type_id(const gpio::pin* pin)
{
    return type_id(pin->chip()) + '#' + std::to_string(pin->pos());
}

////////////////////////////////////////////////////////////////////////////////
}

////////////////////////////////////////////////////////////////////////////////
#endif
